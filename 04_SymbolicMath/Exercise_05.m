%  ------------------------------------------------------------------------
syms phi(t) m g l d
d_phi = diff(phi);
dd_phi = diff(phi, 2);
a = g/l;
b = d/(m*l^2);
phi(t) = dsolve(dd_phi == -a*phi - b*d_phi, phi(0) == 1, d_phi(0) == 0);

%% ========================================================================
%  SubTask 2
%  ------------------------------------------------------------------------
phi_para = subs(phi, {l, m, g, d}, {10, 5, 9.81, 50});

%% ========================================================================
%  SubTask 3
%  ------------------------------------------------------------------------
phi_5 = vpa(phi_para(5), 40);
%  ANSWER:
%  phi(5) = 0.1419576473814292547603698105879010822261

%% ========================================================================
%  SubTask 4
%  ------------------------------------------------------------------------
syms t
x(t) = l*sin(phi_para);
x(t) = subs(x, l, 10);
y(t) = t;
z(t) = -l*cos(phi_para);
z(t) = subs(z, l, 10);
figure('Name', '3D-plot Pendulum Oscillation')
fplot3(x, y, z, [0 40]);
xlabel('l*sin(phi)')
ylabel('t')
zlabel('-l*cos(phi)')
title('Pendulum Oscillation')

