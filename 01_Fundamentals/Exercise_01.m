% Subscript indexing
B_subs = B;
for m=(1:3)
    for n=(1:3)
        if mod(B_subs(m,n), 4) == 0
            B_subs(m,n) = B_subs(m,n)/4;
        end
    end
end
disp(B_subs)

% Linear indexing
B_lin = B;
for j=(1:numel(B_lin))
    if mod(B_lin(j),4)==0
        B_lin(j)= B_lin(j)/4;
    end
end
disp(B_lin)

